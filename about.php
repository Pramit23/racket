				<p>Long back in 1793 a building came up at the present site off Chowringhee between Bijli Talao and Elliots Tank. The Club was originally known as the Fives Court or Gamekhana. The game of Rackets gave way to squash in the 1930s and the Club became the first European institution to open its doors to Indians in 1936 with the election of Raja P.C. Lall of Narayanganj. Mr. A.N. Choudhury soon joined the Club and became its President in 1948. His distinguished son General J.N. Choudhury was a patron from 1969 until his death. The Club, now commonly known as the Squash Club, developed rapidly after 1948. Since then, its popularity has grown by leaps and bounds and has grown in size to accommodate the constantly increasing number of patrons. Today we are proud to be a premier institution catering to the sporting needs of the Kolkata’s young, upcoming & not so young fitness enthusiastic.

The Calcutta Racket Club is the only club in India catering exclusively to the game of squash. It is located in Kolkata, India. It was founded in 1793, making it probably one of the oldest squash clubs in the world.

If you think the name of the Club suggests a pun on the word racquet, you are far from the truth. The Club derives its name from the species of a bird called the “Racket bird” which used to inhabit the trees around the Club when it was founded.

The Calcutta Racket Club is situated centrally, adjacent to Birla Planetarium on Jawaharlal Nehru Road and showcases seven squash courts, of which two are glass backed international standard tournament courts with the largest spectator gallery in India.

The other five courts came up over a period of time after the establishment of the club and are located in the main building.

The Club celebrated its Bicentenary in 1993 and to mark the occasion, invited some of the world’s finest players to participate in a round robin tournament. The Club host to all major squash activities and training in eastern India. It was also the first club in India to host the (5th Asian Men’s Squash Championship) International Squash Championship in 1990. In fact The Calcutta Racket Club hosted the US$ 20,000 Professional Squash Association (PSA) championship in September 2005, the US$30,000 PSA event in Feb 2008 and the US$30,000 PSA event in March 2009 that drew top players from 10 countries.

The J.L. Nehru Road address also has a fully equipped air-conditioned gymnasium complete with hi-tech work out equipment for the fitness-conscious members of the club. It is a small and cozy place to unwind after a hard day’s work. We have a very unique member profile. The membership strength is around 550 out of which more than 150 members are active squash players. Each member knows the other and is quite regular at the Club. Female members are more then welcome.

We have a full-fledged bar. After a game of squash there is a big queue in front of the bar, not for any alcoholic beverage but a glass of fresh lime soda.

